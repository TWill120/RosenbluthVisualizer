import matplotlib.pyplot as plt
import numpy as np
from math import *

# Tau(T) Function
def T(q2):
    return q2/(4 * ((M) ** 2))

# mass of proton
M = .938  # GeV

# Epsilon Function from Q2 and Angle
def eps(q2,angle):
    return (1 + (2 * (1 + (T(q2))) * (np.tan(angle / 2 * pi / 180) ** 2)))**(-1)

# convert GeV to nanobarn
def GeVtonb(x): # GeV-2
    y = x*0.389379 # mb
    z = y * (10**(6)) # nb
    return z

# sigma mott function
def sigmaMott(E, E1, angle):
    num = ((1 / 137) ** 2 * ((np.cos(angle / 2 * np.pi / 180) ** 2) * E1))
    dnm = (4 * E ** 2 * (np.sin(angle / 2 * np.pi / 180) ** 4) * E)
    return num / dnm # * 0.381 * 1E6  # converted to nb from GeV-2

# since Gm can only be between one and 2.7, it is convenient to make it a linspace array
GmList = np.linspace(0, 2.7, 10000)
# Ge is acquired from Gm so its list is blank

# this will plot Ge against Gm (see above)
def plotGe(q2, E, E1, angle, S, Err, Color, Alpha, left, right, down, up):
    GeList = []
    UpGeList = []
    DownGeList = []

    for n in GmList:
        a = 1 + T(q2)
        b = S / GeVtonb(sigmaMott(E, E1, angle))
        c = T(q2) * ((n)**2)
        d = eps(q2, angle)
        GeVal = ((a * b) - (c / d))

        a1 = 1 + T(q2)
        b1 = (S + ((S * 0.0177) + Err))/ GeVtonb(sigmaMott(E, E1, angle))
        c1 = T(q2) * ((n)**2)
        d1 = eps(q2, angle)
        UpGeVal = ((a1 * b1) - (c1 / d1))

        a2 = 1 + T(q2)
        b2 = (S - ((S * 0.0177) + Err)) / GeVtonb(sigmaMott(E, E1, angle))
        c2 = T(q2) * ((n)**2)
        d2 = eps(q2, angle)
        DownGeVal = ((a2 * b2) - (c2 / d2))

        GeList.append(GeVal)
        UpGeList.append(UpGeVal)
        DownGeList.append(DownGeVal)

    plt.xlim(left,right)
    plt.ylim(down,up)
    plt.fill_betweenx(GmList,UpGeList,DownGeList,color=Color,edgecolor=None,alpha=Alpha)
    plt.tight_layout()
    plt.xlabel("G$_E^2$",fontsize=30)
    plt.ylabel("G$_M^2$",fontsize=30)

# this will plot Ge against Gm, but with the normalization that SLAC included on their paper.
def plotGeAdjusted(q2, E, E1, angle, S, Err, Color, Alpha, left, right, down, up):
    GeList = []
    UpGeList = []
    DownGeList = []

    for n in GmList:
        a = 1 + T(q2)
        b = (S - (0.04 * S)) / GeVtonb(sigmaMott(E, E1, angle))
        c = T(q2) * ((n) ** 2)
        d = eps(q2, angle)-(0.04 * eps(q2,angle))
        GeVal = ((a * b) - (c / d))

        a1 = 1 + T(q2)
        b1 = (S + ((S * 0.0177) + Err) - (0.04 * S)) / GeVtonb(sigmaMott(E, E1, angle))
        c1 = T(q2) * ((n) ** 2)
        d1 = eps(q2, angle) - (0.04 * eps(q2,angle))
        UpGeVal = ((a1 * b1) - (c1 / d1))

        a2 = 1 + T(q2)
        b2 = (S - ((S * 0.0177) + Err) - (0.04 * S)) / GeVtonb(sigmaMott(E, E1, angle))
        c2 = T(q2) * ((n) ** 2)
        d2 = eps(q2, angle) - (0.04 * eps(q2,angle))
        DownGeVal = ((a2 * b2) - (c2 / d2))

        GeList.append(GeVal)
        UpGeList.append(UpGeVal)
        DownGeList.append(DownGeVal)

    plt.xlim(left,right)
    plt.ylim(down,up)
    plt.fill_betweenx(GmList,UpGeList,DownGeList,color=Color,edgecolor=None,alpha=Alpha)
    plt.tight_layout()
    plt.xlabel("G$_E^2$",fontsize=30)
    plt.ylabel("G$_M^2$",fontsize=30)

